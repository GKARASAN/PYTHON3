#! Arithmetic operator
print(2+3)
print(2-3)
print(2*3)
print(2**3)
print(2/3)
print(2//3)
print(2%3)

#! Comparetion operator
print(2<3)
print(2>3)
print(2==3)
print(2>=3)
print(2<=3)

#! Assignment operator
a = 2
a -= 3
print(a)
b = 2
b += 3
print(b)
c = 2
c *= 3
print(c)
d = 2
d **= 3
print(d)
e = 2
e /= 3
print(e)
f = 2
f //= 3
print(f)
g = 2
g %= 3
print(g)

#! Identity operator
x = ["2","3"]
y = ["4", "5"]
print(x is y)
print(x is not y)

#! Membership operator
print(x in y)
print(x not in y)

#! Logical operator
print(2<3 and 2<4)
print(2<3 or 2<4)
print(not (2<3 and 2<4))